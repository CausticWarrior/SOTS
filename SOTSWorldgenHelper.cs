				Tile tile4 = Framing.GetTileSafely(i + 1, j + 1);
				if (SkytileValid(tile, mod))
				{
					if (SkytileValid(tile2, mod) && SkytileValid(tile3, mod) && SkytileValid(tile4, mod) && Empty(i, j - 2, 2, 2))
